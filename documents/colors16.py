import turtle
def draw_circle(turtle, color, size, x, y):
    turtle.penup()
    turtle.color(color)
    turtle.fillcolor(color)
    turtle.goto(x,y)
    turtle.begin_fill()
    turtle.circle(size)
    turtle.end_fill()
    turtle.pendown()

tommy = turtle.Turtle()
tommy.shape("turtle")
tommy.speed(500)

draw_circle(tommy, "green", 70, 25, 0)
draw_circle(tommy, "blue", 60, 0, 0)
draw_circle(tommy, "yellow", 50, -25, 0)
draw_circle(tommy, "#ff00ff" , 40, -50, 0)
